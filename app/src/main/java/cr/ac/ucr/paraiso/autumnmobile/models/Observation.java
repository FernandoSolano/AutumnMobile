package cr.ac.ucr.paraiso.autumnmobile.models;

public class Observation {
    private int id;
    private String content;

    public Observation() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
